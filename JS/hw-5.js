// Напишите функцию, которая принимает на вход два числа, а далее следует алгоритму:

// Проверяет, являются ли переданные параметры корректными числами.
// Подсказка: используйте преобразование типов и isNaN().
// Если нет — возвращает строку 'Одно или оба значения не являются числом'.
// Если оба параметра — числа, то возвращает произведение данных чисел.

function multiplyNumbers(a, b) {
    if (isNaN(a) || isNaN(b)) {
        return 'Одно или оба значения не являются числом';
    } else {
        return a * b;
    }
}

console.log(multiplyNumbers(4, 5));
console.log(multiplyNumbers(10, 'abc'));
console.log(multiplyNumbers('xyz', 3));
console.log(multiplyNumbers(7, 2));

// Задание 6
// Напишите функцию, которая выполняет следующий алгоритм:
// Запрашивает у пользователя число.
// Проверяет, является ли введенное значение числом.
// Если значение не является числом, возвращает строку 'Переданный параметр не является числом'.
// Если значение является числом, возвращает строку 'n в кубе равняется <получившееся значение>',
// где n — введенное число, а <получившееся значение> — число, возведенное в куб.
// Проверьте работу функции с числами от 0 до 10.

function cube(n) {
    if (isNaN(n)) {
        return 'Переданный параметр не является числом';
    } else {
        const cubed = Math.pow(n, 3);
        return `${n} в кубе равняется ${cubed}`;
    }
}

for (let i = 0; i <= 10; i++) {
    console.log(cube(i));
}

// Задание 7
// Создайте два объекта, circle1 и circle2, каждый из которых имеет свойство radius.
// Оба объекта должны иметь:
// 1. Метод getArea, который возвращает площадь круга, вычисляемую через радиус.
// 2. Метод getPerimeter, который возвращает периметр окружности.


function createCircle(radius) {
    return {
        radius: radius,
        getArea: function() {
            return Math.PI * Math.pow(this.radius, 2); // Площадь круга: π * r^2
        },
        getPerimeter: function() {
            return 2 * Math.PI * this.radius; // Периметр окружности: 2 * π * r
        }
    };
}

const circle1 = createCircle(5);
const circle2 = createCircle(10);

console.log(`Площадь круга 1: ${circle1.getArea().toFixed(2)}`);
console.log(`Периметр круга 1: ${circle1.getPerimeter().toFixed(2)}`);

console.log(`Площадь круга 2: ${circle2.getArea().toFixed(2)}`);
console.log(`Периметр круга 2: ${circle2.getPerimeter().toFixed(2)}`);